#Написать программу, которая: Создаёт с помощью генератора списков, список случайных целых чисел от **-50** до **50** размером **25** элементов. Находит количество положительных, отрицательных и нулевых элементов в списке. Выводит значения и их (*положительных, отрицательных и нулевых*) количество вместе с процентом от общего количества. Выводит самое большое и самое маленькое значение
import random#
num=[]#
