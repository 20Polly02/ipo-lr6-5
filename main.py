print("Количество отрицательных элементов",neg_num)#
print("Процент от общего количества",neg_num/25*100)#
print("Значения нулевых элементов из списка",zer_list) #
print("Количество нулевых элементов ",zer_num) #
print("Процент от общего количества",zer_num/25*100) #
